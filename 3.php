<?php

function getDaysInMonth($month) {
    // Массив с количеством дней в каждом месяце (не високосный год)
    $daysInMonth = [
        1 => 31,  // Январь
        2 => 28,  // Февраль 
        3 => 31,  // Март
        4 => 30,  // Апрель
        5 => 31,  // Май
        6 => 30,  // Июнь
        7 => 31,  // Июль
        8 => 31,  // Август
        9 => 30,  // Сентябрь
        10 => 31, // Октябрь
        11 => 30, // Ноябрь
        12 => 31  // Декабрь
    ];

    // Проверка корректности номера месяца
    if ($month < 1 || $month > 12) {
        return "Некорректный номер месяца. Введите число от 1 до 12.";
    }

    return $daysInMonth[$month];
}

// Ввод данных
echo "Введите номер месяца (1-12): ";
$month = (int)trim(fgets(STDIN));

echo "Количество дней в месяце: " . getDaysInMonth($month) . "\n";
